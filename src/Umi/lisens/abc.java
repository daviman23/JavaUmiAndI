package Umi.lisens;

public class abc {
    public static void main(String[] args) {
        char i = 'a';
        char j = 'z';
        char l = 'A';
        char m = 'Z';

        while(i <= j) {
            System.out.print(i);
            i++;
        }
        System.out.println();

        while(m >= l) {
            System.out.print(m);
            m--;
        }
    }
}
