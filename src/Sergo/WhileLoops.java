package Sergo;

public class WhileLoops {
    public static void main(String[] args) {
        boolean t = 5 >= 5;
        System.out.println(t);
        int i = 0;
        boolean r = i > 1;
        System.out.println(r);
        while(i < 5){
            System.out.println("Hello" + i);
            i++;
        }
    }
}
